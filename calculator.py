def add_quantum_numbers(q1, q2):
    """
    Add two quantum numbers.
    
    Args:
        q1: First quantum number
        q2: Second quantum number
    
    Returns:
        The sum of the two quantum numbers
    """
    return q1 + q2


if __name__ == "__main__":
    # Example usage
    result = add_quantum_numbers(2, 3)
    print(f"2 + 3 = {result}")
